    //Kollar om det finns en agent som är ansvarig för den alien, om ej ger den fel
    private boolean finnsAgent() {

        try {
            if (Validering.isID(jTxtAnsAge.getText())) {
                int agentId = Integer.parseInt(jTxtAnsAge.getText());
                agent = Agent.getSql(agentId);
                if (agent != null) {

                    return true;
                } else {
                    JOptionPane.showMessageDialog(null, "Finns ingen agent med de uppgavna uppgifterna");
                    return false;
                }
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Kan ej hitta angiven agent");
        } catch (InfException undantag) {
            JOptionPane.showMessageDialog(null, "Fel i sql");
        }
        return false;
    }

    //Kollar så att allt är ifyllt korrekt
    private boolean ifyllt() {
        int k = Integer.parseInt(jTxtPlats.getText());
        boolean platsKoll = false;
        try {

            if (k < Integer.parseInt(Alien.getDb().getAutoIncrement("PLATS", "PLATS_ID")) && k > 0) {
                platsKoll = true;
            } else {
                JOptionPane.showMessageDialog(null, "Platsen måste vara över 0 och finnas i databasen");
                return platsKoll;

            }
        } catch (InfException e) {
            JOptionPane.showMessageDialog(null, "Platsen måste vara över 0 och finnas i databasen");

        }

        if (Validering.validName(jTxtNamn.getText()) && Validering.isID(jTxtAnsAge.getText()) && Validering.isPlats(jTxtPlats.getText()) && Validering.validTelefon(jTxtTelNum.getText()) && (jButtonWorm.isSelected() || jButtonSquid.isSelected() || jButtonBoglodite.isSelected()) && platsKoll) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "Vänligen fyll i alla text fält och EN knapp");
        }
        return false;

    }

