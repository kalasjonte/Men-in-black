/**
 * Använder if-satser för att se vilken knapp som är ifylld.
 * Beroende på vilken knapp som är ifylld kan användaren fylla i olika information.
 * Om det fungerar kommer pop-upfönster upp och om det infe fungerar kommer
 * det också upp felmeddelande.
 * 
 * @param evt 
 */
    private void jBtmRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtmRegActionPerformed
        if (ifyllt() && finnsAgent()) {

            alien = new Alien(jTxtNamn.getText(), "123", jTxtTelNum.getText(), java.time.LocalDate.now().toString(), agent.getId(), Integer.parseInt(jTxtPlats.getText()));
            try {
                alien.setId(id);

                if (jButtonSquid.isSelected()) {
                    if (Validering.notBlankString(jTxtRasVal.getText()) && Validering.isDigit(jTxtRasVal.getText())) {
                        Alien.getDb().insert("INSERT INTO SQUID VALUES (" + id + ",'" + jTxtRasVal.getText() + "');");
                        Alien.getDb().insert("INSERT INTO ALIEN VALUES (" + id + ",'" + alien.getRegistreringsdatum() + "','123','" + alien.getNamn() + "','" + alien.getTelefon() + "'," + alien.getPlats() + "," + alien.getAgent() + ");");
                        JOptionPane.showMessageDialog(null, "Alien är registrerad med ID " +id);
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, "Vänlige  fyll i alla text fält och EN knapp");
                    }
                } else if (jButtonBoglodite.isSelected()) {
                    if (Validering.notBlankString(jTxtRasVal.getText())) {
                        Alien.getDb().insert("INSERT INTO BOGLODITE VALUES (" + id + ",'" + jTxtRasVal.getText() + "');");
                        Alien.getDb().insert("INSERT INTO ALIEN VALUES (" + id + ",'" + alien.getRegistreringsdatum() + "','123','" + alien.getNamn() + "','" + alien.getTelefon() + "'," + alien.getPlats() + "," + alien.getAgent() + ");");
                        JOptionPane.showMessageDialog(null, "Alien är registrerad med ID " + id);
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, "Vänligen fyll i alla text fält och EN knapp");
                    }

                } else if (jButtonWorm.isSelected()) {
                    Alien.getDb().insert("INSERT INTO WORM VALUES (" + id + ");");
                    Alien.getDb().insert("INSERT INTO ALIEN VALUES (" + id + ",'" + alien.getRegistreringsdatum() + "','123','" + alien.getNamn() + "','" + alien.getTelefon() + "'," + alien.getPlats() + "," + alien.getAgent() + ");");
                    JOptionPane.showMessageDialog(null, "Alien är registrerad med ID " + id);
                    dispose();
                }

            } catch (InfException e) {
                System.out.println("fel i sql");
            }
        }


    }//GEN-LAST:event_jBtmRegActionPerformed

