/** Om allt är ifyllt korrekt, kollar det så området finns. Finns områrdet skapas
 * agenten och sedan lägger metoden till resten av information i databasen på agenten.
 * Sedan kommer pop-upfönster med om det fungerande eller inte.
 * @param evt 
 */
    private void jBtnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnOkActionPerformed
        if (ifyllt()) {
            try {
                String sOmrade = Alien.getDb().fetchSingle("Select OMRADES_ID from OMRADE where BENAMNING ='" + jTxtOmrade.getText() + "';");
                int omrade = Integer.parseInt(sOmrade);
                agent = new Agent(jTxtAgnNamn.getText(), jTxtLosen.getText(), jTxtTelefon.getText(), jTxtDate.getText(), id, omrade, adminKoll());

                Alien.getDb().insert("INSERT INTO AGENT VALUES (" + id + ",'" + agent.getNamn() + "','" + agent.getTelefon() + "','" + agent.getAnstallningsdatum() + "','" + agent.getAdministrator() + "','" + agent.getLosenord() + "'," + agent.getOmrade() + ");");
                Alien.getDb().insert("INSERT INTO FALTAGENT VALUES (" + id + ");");
                JOptionPane.showMessageDialog(null, "Agent är registrerad med ID " + id);
                dispose();

            } catch (InfException e) {
                System.out.println("fel i sql");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Kolla att område är rätt ifyllt, kunde ej hitta ett område med det namnet");
            }
        }
    }//GEN-LAST:event_jBtnOkActionPerformed

