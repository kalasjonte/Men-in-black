//Kollar så alla fält är ifyllda
    private boolean ifyllt() {

        if (Validering.validName(jTxtAgnNamn.getText()) && Validering.validPassword(jTxtLosen.getText()) && Validering.validDate(jTxtDate.getText()) && Validering.validName(jTxtOmrade.getText()) && Validering.validTelefon(jTxtTelefon.getText())) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "Vänligen fyll i alla text fält och en 'prick' knapp UNDER admin");
        }
        return false;

    }

